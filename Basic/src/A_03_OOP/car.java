package A_03_OOP;
public class car {
    String carName;
    int curSpeed;

    public String getCarName() {
        return carName;
    }
    public void setCarName(String carName) {
        this.carName = carName;
    }
    public int getCurSpeed() {
        return curSpeed;
    }
    public void setCurSpeed(int curSpeed) {
        this.curSpeed = curSpeed;
    }
}